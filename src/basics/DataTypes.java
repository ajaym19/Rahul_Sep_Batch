package basics;

public class DataTypes {

	public static void main(String[] args) {

		System.out.println("Ajay");
		
		//whenever you create a variable in JAVA
		//you have to define what type of data you want to store
		
		/*
		 * Data Types:
		 * the type of data you want to store
		 * 1. Primitive (8)
		 * 		Integer data: whole number >> 45, 105,
		 * 			byte, short, int, long
		 * 		Floating/Decimal data: 45.57;
		 * 			float, double
		 * 		Character data:  only single characterA, z, 4 @#$%^&*
		 * 			char
		 * 		Boolean (true/false): 
		 * 			boolean
		 * 
		 * 2. Non-Primitive: Arrays, String etc..
		 */
		
		int id = 23;
		String name = "Ajay";
		
		byte b = 34;
		System.out.println(b);
		
		short s1 = 3456;
		System.out.println(s1);
		
		int i1 = 45454;
		System.out.println(i1);
		
		long l1;
		l1 = 45232;
		System.out.println(l1);
		
		float f1 = 45.55f;
		double d1 = 65.45;
		
		char ch1 = 's';
		System.out.println(ch1);
		
		
		boolean isPresent = true; //true
		//boolean isPresent = false; //false
		//boolean isPresent = true; //error
		//boolean isPresent = false; //false
		//boolean isPresent = 1; //true
		//boolean isPresent = 0; //false
		
		
		System.out.println(isPresent);
		
		//will start in 2mins
		
	}

	
}
